	public BundleToolbar(int position, final BindingSource<?> bindingSource, IViewContainer viewContainer) {
		super(position, bindingSource);
			
		buildMainLayout();
		//setCompositionRoot(mainLayout);

		// TODO add user code here
		this.position = position;
		this.viewContainer = viewContainer;
		
		setBindingSource(bindingSource);
		
		btnStartBundle.setDescription("Iniciar Bundle");
		btnStopBundle.setDescription("Parar Bundle");
		
		// set reject button listener
		btnStartBundle.addListener(new ClickListener() {			
			public void buttonClick(ClickEvent event) {
				int index = bindingSource.getIndex();
				
				if (index == 0)
					return;
				
				KonektiFelixResource konektiFelixResourceSelected = (KonektiFelixResource) bindingSource.getItemId();
				
				if (konektiFelixResourceSelected == null)
					return;
				
				if (konektiFelixResourceSelected.getBundle() != null) {
					try {
						konektiFelixResourceSelected.getBundle().start();
					} catch (BundleException e) {
						throw new RuntimeException("¡No se pudo iniciar el bundle seleccionado!", e);
					}
				}
				
				if (listenerStartBundleButton != null)
					listenerStartBundleButton.startBundleButtonClick(new ClickNavigationEvent(event.getButton(), event.getComponent() , null, konektiFelixResourceSelected, 0));					
				
			}
		});
		
		btnStopBundle.addListener(new ClickListener() {			
			public void buttonClick(ClickEvent event) {
				int index = bindingSource.getIndex();
				
				if (index == 0)
					return;
				
				KonektiFelixResource konektiFelixResourceSelected = (KonektiFelixResource) bindingSource.getItemId();
				
				if (konektiFelixResourceSelected == null)
					return;
				
				if (konektiFelixResourceSelected.getBundle() != null) {
					try {
						konektiFelixResourceSelected.getBundle().stop();
					} catch (BundleException e) {
						throw new RuntimeException("¡No se pudo parar el bundle seleccionado!", e);
					}
				}
				
				if (listenerStopBundleButton != null)
					listenerStopBundleButton.stopBundleButtonClick(new ClickNavigationEvent(event.getButton(), event.getComponent() , null, konektiFelixResourceSelected, 0));					
				
			}
		});
		
		
		btnOpenBundle.addListener(new ClickListener() {			
			public void buttonClick(ClickEvent event) {
				KonektiFelixResource konektiFelixResourceSelected = (KonektiFelixResource) bindingSource.getItemId();
								
				if (listenerOpenBundleButton != null)
					listenerOpenBundleButton.openBundleButtonClick(new ClickNavigationEvent(event.getButton(), event.getComponent() , null, konektiFelixResourceSelected, 0));					
				
			}
		});
	
}

