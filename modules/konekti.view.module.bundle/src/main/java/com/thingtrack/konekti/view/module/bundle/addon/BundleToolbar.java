	public class ClickNavigationEvent extends ClickEvent {
		private int index;
		private Object register;
		
		public ClickNavigationEvent(Button button, Component source) {
			button.super(source);
			
		}

		public ClickNavigationEvent(Button button, Component source, MouseEventDetails details) {
			button.super(source, details);
			
		}

		public ClickNavigationEvent(Button button, Component source, MouseEventDetails details, Object register, int index) {
			button.super(source, details);
			
			this.index = index;
			this.register = register;
		}

		public int getIndex() {
			return this.index;
			
		}
		
		public Object getRegister() {
			return this.register;
			
		}
		
	  }
	
	@Override
	public void setBindingSource(BindingSource<?> bindingSource) {
		this.bindingSource = bindingSource;
		
		// add change index binding source
		if (bindingSource != null) {
			bindingSource.addListenerToolBar((IndexChangeListener)this);
			
		}
		
	}
	
	@Override
	public void bindingSourceIndexChange(IndexChangeEvent event) {
		if (bindingSource != null) {
			KonektiFelixResource konektiFelixResourceSelected = (KonektiFelixResource)event.getRegister();
			
			if (konektiFelixResourceSelected == null)
				return;
			
			if (konektiFelixResourceSelected.getBundle() == null)
				return;
			
			if (konektiFelixResourceSelected.getBundle().getState() == Bundle.ACTIVE) {
				btnStartBundle.setEnabled(false);
				btnStopBundle.setEnabled(true);
			}
			else if (konektiFelixResourceSelected.getBundle().getState() == Bundle.RESOLVED) {
				btnStartBundle.setEnabled(true);
				btnStopBundle.setEnabled(false);
			}
			else {
				btnStartBundle.setEnabled(false);
				btnStopBundle.setEnabled(false);
			}
		}
		
	}
	
