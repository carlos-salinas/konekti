package com.thingtrack.konekti.view.web.form.field;

import java.util.List;

import org.osgi.framework.BundleContext;
import org.osgi.framework.FrameworkUtil;
import org.osgi.framework.ServiceReference;
import org.vaadin.addon.customfield.CustomField;

import com.thingtrack.konekti.domain.Warehouse;
import com.thingtrack.konekti.service.api.ProductService;
import com.thingtrack.konekti.service.api.WarehouseService;
import com.thingtrack.konekti.view.addon.data.BindingSource;
import com.vaadin.annotations.AutoGenerated;
import com.vaadin.data.Property;
import com.vaadin.ui.CheckBox;
import com.vaadin.ui.Table;
import com.vaadin.ui.VerticalLayout;

@SuppressWarnings("serial")
public class ProductWarehouseCollectionField extends CustomField {

	@AutoGenerated
	private VerticalLayout mainLayout;
	@AutoGenerated
	private Table warehousesTable;

	/*- VaadinEditorProperties={"grid":"RegularGrid,20","showGrid":true,"snapToGrid":true,"snapToObject":true,"movingGuides":false,"snappingDistance":10} */
	
	private BindingSource<Warehouse> warehousesBindingSource;
	
	private List<Warehouse> assignedWarehouse;
	
	private ProductService productService;
	private WarehouseService warehouseService;

	/**
	 * The constructor should first build the main layout, set the
	 * composition root and then do any custom initialization.
	 *
	 * The constructor will not be automatically regenerated by the
	 * visual editor.
	 */
	public ProductWarehouseCollectionField() {
		buildMainLayout();
		setCompositionRoot(mainLayout);

		// TODO add user code here
		
		//Retrieve enterprise services
		getServices();
		
		warehousesBindingSource = new BindingSource<Warehouse>(Warehouse.class);
		
		warehousesTable.setContainerDataSource(warehousesBindingSource);
		warehousesTable.addGeneratedColumn(ProductAssignmentColumn.PRODUCT_COLUMN_ID, new ProductAssignmentColumn());
		warehousesTable.setVisibleColumns(new String[]{"code", "description", ProductAssignmentColumn.PRODUCT_COLUMN_ID});
		warehousesTable.setColumnHeaders(new String[] { "Code", "Descripci√≥n", "Activo" } );
		
	}
	
	@Override
	public void setPropertyDataSource(Property newDataSource) {

		if(newDataSource.getValue() instanceof List){
			
			List<Warehouse> warehouses = (List<Warehouse>) newDataSource.getValue();
			
			//Already organziations assigned to parent vehicle
			assignedWarehouse = warehouses;
			
			//Clean first the biding source
			warehousesBindingSource.removeAllItems();
			try {
				//Add all available organizations 
				warehousesBindingSource.addAll(warehouseService.getAll());
			} catch (Exception e) {
				e.printStackTrace();
			}
		}
		
		
		super.setPropertyDataSource(newDataSource);
	}
	
	@Override
	public Class<?> getType() {

		if(getPropertyDataSource() instanceof Property)
			return getPropertyDataSource().getType();
		
		return List.class;
	}
	
	@Override
	public Object getValue() {		
		return assignedWarehouse;
		
	}

	@AutoGenerated
	private VerticalLayout buildMainLayout() {
		// common part: create layout
		mainLayout = new VerticalLayout();
		mainLayout.setImmediate(false);
		mainLayout.setWidth("100%");
		mainLayout.setHeight("100%");
		mainLayout.setMargin(false);
		
		// top-level component properties
		setWidth("100.0%");
		setHeight("100.0%");
		
		// Organizationstable
		warehousesTable = new Table();
		warehousesTable.setImmediate(false);
		warehousesTable.setWidth("100.0%");
		warehousesTable.setHeight("100.0%");
		mainLayout.addComponent(warehousesTable);
		mainLayout.setExpandRatio(warehousesTable, 1.0f);
		
		return mainLayout;
	}
	
	@SuppressWarnings("unchecked")
	public void getServices(){
		
		BundleContext bundleContext = FrameworkUtil.getBundle(this.getClass()).getBundleContext();
		
		ServiceReference productServiceReference = bundleContext.getServiceReference(ProductService.class.getName());
		productService =  ProductService.class.cast(bundleContext.getService(productServiceReference));
		
		ServiceReference warehouseServiceReference = bundleContext.getServiceReference(WarehouseService.class.getName());
		warehouseService = WarehouseService.class.cast(bundleContext.getService(warehouseServiceReference));
		
	}
	
	
	private class ProductAssignmentColumn implements Table.ColumnGenerator{

		static final String PRODUCT_COLUMN_ID = "warehouse-assignment";

		@Override
		public Object generateCell(Table source, Object itemId, Object columnId) {
			
			CheckBox assignmentColumn = new CheckBox();
			
			final Warehouse warehouseItem = (Warehouse) itemId;
			
			assignmentColumn.setValue(assignedWarehouse.contains(warehouseItem));
				
			assignmentColumn.addListener(new ValueChangeListener() {
				
				@Override
				public void valueChange(Property.ValueChangeEvent event) {
				
					boolean assignmentValue = (Boolean) event.getProperty().getValue();
					
					if(assignmentValue)
						assignedWarehouse.add(warehouseItem);
					
					else assignedWarehouse.remove(warehouseItem);
					
				}
				
			});
			
			return assignmentColumn;
		}
		
		
	}
	
}
